package Practice.basics.力扣;

/**
 * @Title: ListNode
 * @Author Mr.罗
 * @Package Practice.basics.力扣
 * @Date 2023/10/7 20:18
 * @description: 两数相加
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
